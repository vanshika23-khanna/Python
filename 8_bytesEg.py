# Bytes Example

print("Bytes Example:")
b = bytes([65, 66, 67, 68])
print(b) 

for byte in b:
    print(byte, end=' ') 

# Bytearray Example

print("Bytearray Example:")

ba = bytearray([65, 66, 67, 68])
print(ba) 

# Memoryview Example

print("Memoryview Example:")

b_mv = bytes([65, 66, 67, 68, 69])
print(b_mv)

